namespace LfrlAnvil.Chrono;

/// <summary>
/// Represents an ISO 8601 day of week.
/// </summary>
public enum IsoDayOfWeek : byte
{
    /// <summary>
    /// Unknown day of week.
    /// </summary>
    Unknown = 0,

    /// <summary>
    /// Represents monday.
    /// </summary>
    Monday = 1,

    /// <summary>
    /// Represents tuesday.
    /// </summary>
    Tuesday = 2,

    /// <summary>
    /// Represents wednesday.
    /// </summary>
    Wednesday = 3,

    /// <summary>
    /// Represents thursday.
    /// </summary>
    Thursday = 4,

    /// <summary>
    /// Represents friday.
    /// </summary>
    Friday = 5,

    /// <summary>
    /// Represents saturday.
    /// </summary>
    Saturday = 6,

    /// <summary>
    /// Represents sunday.
    /// </summary>
    Sunday = 7
}
