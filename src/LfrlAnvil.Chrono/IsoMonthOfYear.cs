    /// Represents january.
    /// </summary>
    January = 1,

    /// <summary>
    /// Represents february.
    /// </summary>
    February = 2,

    /// <summary>
    /// Represents march.
    /// </summary>
    March = 3,

    /// <summary>
    /// Represents april.
    /// </summary>
    April = 4,

    /// <summary>
    /// Represents may.
    /// </summary>
    May = 5,

    /// <summary>
    /// Represents june.
    /// </summary>
    June = 6,

    /// <summary>
    /// Represents july.
    /// </summary>
    July = 7,

    /// <summary>
    /// Represents august.
    /// </summary>
    August = 8,

    /// <summary>
    /// Represents september.
    /// </summary>
    September = 9,

    /// <summary>
    /// Represents october.
    /// </summary>
    October = 10,

    /// <summary>
    /// Represents november.
    /// </summary>
    November = 11,

    /// <summary>
    /// Represents december.
    /// </summary>
    December = 12
}
